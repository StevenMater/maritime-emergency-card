      // ══ LANGUAGE ════════════════════════════════════════════════════════
      let currentLang = "nl"

      function setLang(lang) {
        currentLang = lang
        document.documentElement.lang = lang
        document
          .querySelectorAll(".lang-btn")
          .forEach((b) =>
            b.classList.toggle(
              "active",
              b.getAttribute("onclick") === `setLang('${lang}')`,
            ),
          )
        document.querySelectorAll("[data-i18n]").forEach((el) => {
          const key = el.getAttribute("data-i18n")
          if (T[lang][key] !== undefined) {
            el.innerHTML = T[lang][key]
          }
        })
        document.querySelectorAll("[data-tip-i18n]").forEach((el) => {
          const key = el.getAttribute("data-tip-i18n")
          if (T[lang][key] !== undefined) {
            if (el.classList.contains("tip-btn")) {
              el.setAttribute("data-tip", T[lang][key])
            } else {
              el.setAttribute("aria-label", T[lang][key])
            }
          }
        })
        document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
          const key = el.getAttribute("data-i18n-aria")
          if (T[lang][key] !== undefined) el.setAttribute("aria-label", T[lang][key])
        })
        document.getElementById("site-title").innerHTML =
          '<span class="brand-title-text">Mare<span>Safe</span></span><span class="brand-title-short">M<span>S</span></span>'
        renderContactEditor()
        buildProtocols()
        buildSignals()
        update()
        const vw = document.getElementById("version-warning")
        if (vw && vw.style.display !== "none") showVersionWarning()
        saveToStorage()
      }

